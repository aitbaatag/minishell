#include <stdio.h>

void	ft_bzero(void *dst, size_t n)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = dst;
	while (n > i)
	{
		*(ptr + i) = 0;
		i++;
	}
}
